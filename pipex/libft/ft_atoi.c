#include "libft.h"

long int	ft_atoi(const char *str)
{
	int			digit;
	int			sign;
	long int	result;
	int			i;

	sign = 1;
	result = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		digit = str[i] - '0';
		result = result * 10 + digit;
		i++;
	}
	return (sign * result);
}
